def get_dynasty_config(dynasty_id: str) -> dict | None:
    """获取指定朝代的配置"""
    for dynasty in DYNASTIES:
        if dynasty["id"] == dynasty_id:
            return dynasty
    return None


def get_prototypes_for_dynasty(dynasty_id: str) -> list:
    """获取指定朝代的所有原型（目前所有朝代共用同一套原型）"""
    return PROTOTYPES


def get_characters_for_dynasty(dynasty_id: str) -> list:
    """获取指定朝代的所有人物"""
    return [f for f in HISTORICAL_FIGURES if f["dynasty"] == dynasty_id]


def get_questions_for_dynasty(dynasty_id: str) -> list:
    """获取指定朝代的所有题目"""
    return DYNASTY_QUESTIONS.get(dynasty_id, [])


def get_all_dynasty_ids() -> list:
    """获取所有朝代ID列表"""
    return [d["id"] for d in DYNASTIES]
