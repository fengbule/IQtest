from __future__ import annotations


DIFFICULTY_WEIGHT_MAP = {
    "easy": 1.0,
    "medium": 1.2,
    "hard": 1.5,
}


def build_question(
    order_no: int,
    category: str,
    difficulty: str,
    prompt: str,
    options: list[str],
    correct_option: str,
    explanation: str,
    tags: list[str],
    estimated_seconds: int,
) -> dict:
    return {
        "order_no": order_no,
        "category": category,
        "difficulty": difficulty,
        "difficulty_weight": DIFFICULTY_WEIGHT_MAP[difficulty],
        "prompt": prompt,
        "option_a": options[0],
        "option_b": options[1],
        "option_c": options[2],
        "option_d": options[3],
        "correct_option": correct_option,
        "explanation": explanation,
        "tags": ",".join(tags),
        "estimated_seconds": estimated_seconds,
        "is_active": True,
    }


